						<p>The Cosmetology course is designed to train the student in the practical skills,
theoretical knowledge, and professional attitudes necessary to be employed or self-
employed entrepreneur. The Cosmetology department objectives include the
following;
<ul>
	<li>Preparing students diligently in order to pass NITA grade examinations and
KNEC certification.</li>
	<li>Achieving proficiency to perform skills in the areas of hair cutting, hair
styling, hair coloring, skin care, make-up application, manicuring and
Massage.</li>
	<li>Being able to communicate effectively with colleagues, supervisors and
guests.</li>
	<li>Being able to project professionalism and act courteously.</li>
	<li>Being able to perform basic analytical skills to be able to advise clients on
total look concepts.</li>
	<li>Being able to apply academic learning, technical information and related
matter to assure sound judgements, decisions and procedures.</li>
</ul>
	</p>

	<p>After the graduate has successfully passed their Exams, they will be qualified to;
<ul>
	<li>work as a cosmetologist in the Hospitality industry, Defence ministries,
private members clubs and so on.</li>
	<li>Perform tasks like cut hair, perform hair styling, perform all phases of
manicuring and pedicure , apply make-up, give skin care and give massage
therapy</li>
	<li>Uwezo College School of Hair dressing and Beauty offers both professional
courses lasting more than 6 months and Jobs specific skills training that take 3
months.</li>
	
</ul>
	</p>
