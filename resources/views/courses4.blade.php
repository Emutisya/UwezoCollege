						<p>Uwezo College from the outset identified social work and community development
as an area lacking in properly trained professionals based on a number of media
reports on mistreatment of the sick, elderly, special needs children, HIV aids
victims etc

<p>Social and Community Development is a course that covers a wide range of
contemporary subjects and topics. What these things have in common is that they
will help one to tackle the social problems that matter most such as homelessness,
drug-use, violence, mental health, and many more.</p>
<p>TSocial workers are responsible for helping individuals, families, and groups of
people to cope with problems they&#39;re facing to improve their patients&#39; lives. One
aspect of this is teaching skills and developing mechanisms for patients to rely on
to better their lives and experiences.</p>

<p>Community development usually has three major elements:</p>
<ul>
	<li>community
members&#39; well-being or welfare involving both material sufficiency and non-
economic aspects of living such as health and education;</li>
	<li>resource development,
involving increased production and efficiency;</li>
	<li>organizational development</li>

	
</ul>
	</p>

	<p>Upon completion of the prescribed course of study for Diploma in Community
Development and Social Work the graduate will have acquired knowledge, skills
and abilities to be able to:
<ul>
	<li>Use development knowledge skills and abilities attained to support Development organizations control principles and procedures;</li>
	<li>Describe development concepts, principles and procedures in development
organisations</li>
	<li>Select and apply development paradigms in appropriate development
situations;</li>
<li>Apply various development skills to solve problems in various economics,
political and social areas</li>
	
</ul>
	</p>
