						<p>You will often hear people saying the World is going digital and that to fit in, one
must be computer literate! It is an open secret that there is ever growing demand
among all professions to employ computing skills in order to improve performance
and efficiency at all levels of organization operations.

<p>Uwezo College upon realization of the need for IT skills necessary in all spheres of
live has come with professional courses aimed at equipping the students to be more
than a user.</p>
<p>In addition they have come up courses that are aimed at conferring basic skills
such as computer packages. Indeed Students in all study disciplines are expected to
undergo this course if they don’t have a certificate for the same from reputable
colleges.</p>

<p>To this end our programs are designed to achieve the following objectives;</p>
<ul>
	<li>Assist beginners in becoming proficient users of computers and related ICT
equipment (eg printers)</li>
	<li>Equip students with necessary knowledge and exposure to be able to pass
National Exams from KNEC</li>
	<li>Enable student to make positive contributions to community and society by
applying skills and abilities learned</li>
	<li>Prepare students who plan to pursue their studies to graduate courses</li>
	<li>Prepare students to make decisions related to work that demonstrate
understanding of the importance of being an ethical computing professional</li>
	<li>Equip students to apply communication skills to effectively promote ideas,
goals or products</li>

	
</ul>
	</p>
