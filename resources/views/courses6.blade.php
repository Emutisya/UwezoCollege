                    <p>From earl 1990s the Government started encouraging promotion of local
                        technical schools to Universities in order absolve a lot students from 8-4-4
                        qualified for University but could not access due to limited slots in the two
                        Universities available then, Nairobi and Kenyatta Universities.

                        <p>Unfortunately
                            these were not replaced immediately with enough middle technical colleges to
                            offer skilled training in areas like Masonry, plumbing, electrical wiring , carpentry
                            among others.</p>
                        <p>The result has been acute shortage of these very essential skills.
                            Uwezo College identified that gap and is now training many young in these fields.</p>

                        <p>By offering these courses Uwezo College expects to achieve the following
                            objectives;</p>
                        <ul>
                            <li>ATo offer top notch skills training that enable students to pass National
                                examinations set by KNEC or NITA</li>
                            <li>To inculcate a culture of professionalism that will gain the trainees respect
                                to be called Technicians and not just ‘jua kali artsan’</li>
                            <li>To offer training in Technical fields that are consistent with the needs of
                                industry as well as in keeping with national goals (Agenda 4 - affordable
                                housing).</li>
                            <li>To provide opportunities and experiences that promote the development
                                of self as well as that of the nation.</li>




                        </ul>
                    </p>



                    <p>It is expected that once the students complete our training programs will be able to;</p>
                    <ul>
                        <li>Employ tools and materials appropriate to their purpose and function and in
                            keeping with safe work practices within the construction industry.</li>
                        <li>Relate ethical work practices to self as a tradesperson in the construction
                            industry.</li>
                        <li>Solve construction industry problems by assessing the situation and applying
                            problem solving tools and processes.</li>
                        <li>TCommunicate effectively with a variety of customers, clients and co-workers
                            with the context of the particular trades sector.</li>

                        <li>Interpret relevant codes, specifications plans and construction processes.</li>
                        <li>Work in compliance with the theory and the accepted principles and
                            practices of the construction industry trades.</li>
                        <li>Explain the interrelationships between construction projects and other
                            disciplines including architectural, structural, mechanical, electrical, and
